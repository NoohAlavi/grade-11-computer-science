    private void btnCheckoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckoutActionPerformed
        Boolean isEligibleForDiscount = false;

        int numberOfDonuts = getItemAmount(txtDonuts);
        int numberOfCoffees = getItemAmount(txtCoffee);
        int numberOfHimbits = getItemAmount(txtHimbits);
        
        // check for donut discount
        if (numberOfDonuts < 2) {
            lblDiscountTitle.setVisible(true);
            lblDonutDiscount.setVisible(true);
        } else {
            isEligibleForDiscount = true;
        }

        // calculate total price
        double[] prices = calculateTotalPrice(isEligibleForDiscount);

        // hide main GUI
        pnlModifyOrder.setVisible(false);
        pnlCompleteOrder.setVisible(true);
        
        // output info to user        
        lblCoffeeReceipt.setText("COFFEES (x" + numberOfCoffees + ").......... $" + df.format(prices[0]));
        lblDonutsReceipt.setText("DONUTS (x" + numberOfDonuts + ").......... $" + df.format(prices[1]));
        lblHimbitsReceipt.setText("HIMBITS Â© (x" + numberOfHimbits + ").......... $" + df.format(prices[2]));
        lblSubtotalReceipt.setText("SUBTOTAL.......... $" + df.format(prices[3]));
        lblDiscountReceipt.setText("DISCOUNT AMOUNT (15%).......... $" + df.format(prices[4]));
        lblTaxAmountReceipt.setText("TAX AMOUNT (13%).......... $" + df.format(prices[5]));
        lblTotalPriceReceipt.setText("TOTAL PRICE.......... $" + df.format(prices[6]));
        
        // show lblDiscount15 if order is >=85 and <100
        if (prices[3] >= START_SHOW_DISCOUNT && prices[3] < DISCOUNT_MINIMUM) {
            lblDiscountTitle.setVisible(true);
            lblDiscount15.setVisible(true);
        }
        
        // only show "lblDiscountReceipt" if user is getting a discount
        if (prices[4] > 0) {
            lblDiscountReceipt.setVisible(true);
        }
    }//GEN-LAST:event_btnCheckoutActionPerformed

