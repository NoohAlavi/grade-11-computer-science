    final double COFFEE_PRICE = 1.79;
    final double DONUTS_PRICE = .99;
    final double HIMBITS_PRICE = 3.29;
    
    final double TAX_RATE = 0.13; // 13%
    final double DISCOUNT_RATE = 0.15; // 15%
    
    final double START_SHOW_DISCOUNT = 85; // if user subtotal is this #, tell them about discount
    final double DISCOUNT_MINIMUM = 100; // if user has over this #, apply DISCOUNT_RATE
    
    double subtotal = 0;
    double discountedAmount = 0;
    double taxAmount = 0;
    double finalPrice = 0;
    
    private int getItemAmount(javax.swing.JTextField txtItem) {
        return Integer.parseInt(txtItem.getText());
    }

    private double[] calculateTotalPrice(Boolean isEligibleForDiscount) {
        double discountAmount = 0;
        
        // Calculate subtotal
        
        double totalDonutsPrice = getItemAmount(txtDonuts) * DONUTS_PRICE;
        double totalHimbitsPrice = getItemAmount(txtHimbits) * HIMBITS_PRICE;
        double totalCoffeePrice = getItemAmount(txtCoffee) * COFFEE_PRICE;

        if (isEligibleForDiscount) {
            totalDonutsPrice -= DONUTS_PRICE; // remove price of ONE donut
        }
        
        subtotal = totalCoffeePrice + totalDonutsPrice + totalHimbitsPrice;

        // TODO: Apply donut discount if applicable
        
        
        // Apply discount if applicable
        if (subtotal >= DISCOUNT_MINIMUM) {
            discountAmount = subtotal * DISCOUNT_RATE;
        }
        discountedAmount = subtotal - discountAmount;
        
        // Calculate tax amount
        taxAmount = discountedAmount * TAX_RATE;

        // Calculate total price
        finalPrice = discountedAmount + taxAmount;
        
        // Return all the prices
        double[] prices = {totalCoffeePrice, totalDonutsPrice, totalHimbitsPrice, 
            subtotal, discountAmount, taxAmount, finalPrice};
        return prices;
    }
    
    private void addItem(javax.swing.JTextField txtItem) {
        txtItem.setText(
            String.valueOf(
                getItemAmount(txtItem) + 1
            )
        );
    }
    
    private void subItem(javax.swing.JTextField txtItem) {
        int updatedAmount = getItemAmount(txtItem) - 1;
        
        if (updatedAmount >= 0) { //make sure it never goes into a negative number
            txtItem.setText(
                String.valueOf(
                    getItemAmount(txtItem) - 1
                )
            );
        }
    }
    
